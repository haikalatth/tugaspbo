    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
    
    
    if("".equals(nis.getText()) || "".equals(alamat.getText()) ||"".equals(telpon.getText()) ||"".equals(berat.getText()) ||"".equals(tinggi.getText()) || "".equals(kelas.getText()) || "".equals(nama.getText()) || "".equals(email.getText())){
    JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
    }else {
        String JK = "";
        if(lanang.isSelected()){
            JK = "L";
        }else{
            JK = "P";
        }
        
        String SQL = "INSERT INTO t_siswa (NIS,NamaSiswa,JenisKelamin,TinggiBadan,BeratBadan,Kelas,Telp,Email,Alamat)"
                + "VALUES('"+nis.getText()+"','"+nama.getText()+"','"+JK+"','"+tinggi.getText()+"','"+berat.getText()+"',"
                + "'"+kelas.getText()+"','"+telpon.getText()+"','"+email.getText()+"','"+alamat.getText()+"')";
        int status = KoneksiDB.execute(SQL);
        if(status == 1){
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        } else{
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan", "Gagal", JOptionPane.WARNING_MESSAGE);
        }
   
    }
    }//GEN-LAST:event_bsaveActionPerformed

    private void bdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeleteActionPerformed
    
        int baris = tblData.getSelectedRow();
        if (baris != -1){
            String NIS = tblData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM t_siswa WHERE NIS='"+NIS+"'";
            int status = KoneksiDB.execute(SQL);
            if(status==1){
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus", "Gagal", JOptionPane.WARNING_MESSAGE);
                
            }
        }else{
            JOptionPane.showMessageDialog(this, "Pilih baris data terlebih dahulu","Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bdeleteActionPerformed

