    public void selectData() {
        
       String kolom[] = {"NIS","NamaSiswa","JenisKelamin","TinggiBadan","BeratBadan","Kelas","Telp","Email","Alamat"};
       DefaultTableModel dtm = new DefaultTableModel(null, kolom);
       String SQL = "SELECT * FROM t_siswa";
       ResultSet rs = KoneksiDB.executeQuery(SQL);
       try{
           while(rs.next()){
               String NIS = rs.getString(1);
               String NamaSiswa = rs.getString(2);
               String JenisKelamin = "";
               if("L".equals(rs.getString(3))){
                   JenisKelamin = "Laki-Laki";
               } else {
                   JenisKelamin = "Perempuan";
               }
               String TinggiBadan = rs.getString(4);
               String BeratBadan = rs.getString(5);
               String Kelas = rs.getString(6);
               String Telp = rs.getString(7);
               String Email = rs.getString(8);
               String Alamat = rs.getString(9);
               String data[] = {NIS,NamaSiswa,JenisKelamin,TinggiBadan,BeratBadan,Kelas,Telp,Email,Alamat};
               dtm.addRow(data);
           }
       } catch(SQLException ex){
           Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null,ex);
       }
       tblData.setModel(dtm);
        
    }
